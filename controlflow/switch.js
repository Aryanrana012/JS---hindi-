// switch case statement syntax

const month = 3;

switch (month) {
    case 1:
        console.log("jan");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("mar"); 
        break;

    default:
        console.log("your month is may- december ");
        break;
}